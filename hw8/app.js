class Gallery {
  constructor({ items, parentNode, defaultActiveItem }) {
    this.items = items;
    this.parentNode = parentNode;
    this.defaultActiveItem = defaultActiveItem;
  }

  build() {
    const fullview = document.createElement("div"); //—Å—Ç–≤–æ—Ä—é—î–º–æ –±–ª–æ–∫ –∑ –∫–∞—Ä—Ç–∏–Ω–∫–æ—é fullview
    fullview.classList.add("fullview");
    fullview.insertAdjacentHTML(
      "afterbegin",
      `<img src="img/fullview-${this.defaultActiveItem}.jpg" alt="alt text ${
        this.defaultActiveItem
      }">`
    );
    this.parentNode.append(fullview);

    let list = this.items.map((el, idx) => {
      return `<li> <img ${                                               //—Å—Ç–≤–æ—Ä—é—î–º–æ –µ–ª–µ–º–Ω–µ–Ω—Ç–∏ LI
        idx == this.defaultActiveItem - 1 ? `class="img-active"` : ""    //–¥–æ–¥–∞—ç–º–æ –∫–ª–∞—Å –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—ã –¥–ª—è –ø–æ—á–∞—Ç–∫–æ–≤–æ—ä –∫–∞—Ä—Ç–∏–Ω–∫–∏
      }src=${el.preview} data-fullview=${el.fullview} alt=${el.alt}></li>`;
    });
    this.parentNode.insertAdjacentHTML(   //–¥–æ–¥–∞—î–º–æ –±–ª–æ–∫ –∑ –∫–∞—Ä—Ç–∏–Ω–∫–∞–º–∏ –≤ –æ—Å–Ω–æ–≤–Ω–∏–π –±–ª–æ–∫
      "beforeend",
      `<ul class='preview'>` + list.join("") + `</ul>`
    );

    const preview = document.querySelector("ul");
    preview.addEventListener("click", this.handlePreviewClick); //—ñ–≤–µ–Ω—Ç –∫–ª—ñ–∫—É –Ω–∞ –æ–¥–Ω—É –∑ –∫–∞—Ä—Ç–∏–Ω–æ–∫
  }

  handlePreviewClick({ target }) {
    if (target.nodeName !== "IMG") return;

    const currentActiveLink = this.parentNode.querySelector(".img-active"); //—à—É–∫–∞—ç–º–æ –∞–∫—Ç–∏–≤–Ω–∏–π –µ–ª–µ–º–µ–Ω—Ç
    if (currentActiveLink) {
      //—è–∫—â–æ —Ç–∞–∫–∏–π —ç - –≤–∏–¥–∞–ª—è—ç–º–æ –π–æ–≥–æ –∫–ª–∞—Å
      currentActiveLink.classList.remove("img-active");
    }
    target.classList.add("img-active"); //–¥–æ–¥–∞—î–º–æ –∞–∫—Ç–∏–≤–Ω—ñ—Å—Ç—å –Ω–æ–≤–æ–º—É –µ–ª–µ–º–µ–Ω—Ç—É

    const fullImg = this.parentNode.querySelector(".fullview>img");
    fullImg.setAttribute("src", target.dataset.fullview); //–≤–∫–∞–∑—É—î–º–æ –±–ª–æ–∫—É –∑ –≤–µ–ª–∏–∫–æ—é –∫–∞—Ä—Ç–∏–Ω–∫–æ—é –¥–∞–Ω—ñ –∑ –∞–∫—Ç–∏–≤–Ω–æ–≥–æ –µ–ª–µ–º–µ–Ω—Ç–∞
    fullImg.setAttribute("alt", target.getAttribute("alt"));
  }
}

