`use strict`;

const products = {
  bread: 10,
  milk: 15,
  apples: 20,
  chicken: 50,
  cheese: 40
};

function Cashier(name, productDatabase) {
  this.name = name;
  this.productDatabase = productDatabase;
  this.customerMoney = 0;

  this.getCustomerMoney = function(value) {    //–ø—Ä–∏–º–π–º–∞—î —Å—É–º—É —ñ –ø–µ—Ä–µ–¥–∞—î —ó—ó –≤ –ø–æ–ª–µ "–≥—Ä–æ—à—ñ –ü–æ–∫—É–ø—Ü—è" 
    this.customerMoney = value;
  };
  this.countTotalPrice = function(order) {     //–ø—Ä–∏–π–º–∞—î –æ–±'—î–∫—Ç —Å–ø–∏—Å–æ–∫ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è, —Ä–∞—Ö—É—î —Å—É–º—É –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è —ñ –ø–æ–≤–µ—Ä—Ç–∞—î —ó—ó
    let totalPrice = 0;
    const orderArr = Object.keys(order);
    const productDatabaseArr = Object.keys(this.productDatabase);
    for (let keyBase of productDatabaseArr) {
      for (let keyOrder of orderArr) {
        if (keyBase === keyOrder) {          
          totalPrice += productDatabase[keyBase] * order[keyOrder];
          break;
        }
      }
    }
    return totalPrice;
  };
  this.countChange = function(totalPrice) {    //–ø—Ä–∏–π–º–∞—î —Å—É–º—É –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è (countTotalPrice), —Ä–∞—Ö—É—î —ñ –ø–æ–≤–µ—Ä—Ç–∞—î —Ä—ñ–∑–Ω–∏—Ü—é –≥—Ä–æ—à–µ–π –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ —ñ —Å—É–º–∏ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è
    if (this.customerMoney > totalPrice) {
      return this.customerMoney - totalPrice ;
    } else {                                   //—è–∫—â–æ —Å—É–º–∏ –ø–µ—Ä–µ–¥–∞–Ω–æ—ó –ø–æ–∫—É–ø—Ü–µ–º –Ω–µ –¥–æ—Å—Ç–∞—Ç–Ω—å–æ, –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ null
      return null;
    }
  };
  this.onSuccess = function(change) {           //–ø—Ä–∏–π–º–∞—î —Å—É–º—É –∑–¥–∞—á—ñ (countChange), –≤–∏–≤–æ–¥–∏—Ç—å –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è
    console.log(`–°–ø–∞—Å–∏–±–æ –∑–∞ –ø–æ–∫—É–ø–∫—É, –≤–∞—à–∞ —Å–¥–∞—á–∞ ${change}!`);
  };
  this.onError = function() {                   //–ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –Ω–µ –¥–æ—Å—Ç–∞—Ç–Ω—å–æ –∫–æ—à—Ç—ñ–≤
    console.log("–û—á–µ–Ω—å –∂–∞–ª—å, –≤–∞–º –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç –¥–µ–Ω–µ–≥ –Ω–∞ –ø–æ–∫—É–ø–∫–∏");
  };
  this.reset = function() {                     //–æ–±–Ω—É–ª—è—î –≥—Ä–æ—à—ñ –ø–µ—Ä–µ–¥–∞–Ω—ñ –ø–æ–∫—É–ø—Ü–µ–º
    this.customerMoney = 0;
  };
}

/* –ó–∞–∫–∞–∑ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Ö—Ä–∞–Ω–∏—Ç—Å—è –≤ –≤–∏–¥–µ –æ–±—ä–µ–∫—Ç–∞ —Å–ª–µ–¥—É—é—â–µ–≥–æ —Ñ–æ—Ä–º–∞—Ç–∞. "–∏–º—è-–ø—Ä–æ–¥—É–∫—Ç–∞":"–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ-–µ–¥–∏–Ω–∏—Ü" */
const order = {
  bread: 2,
  milk: 2,
  apples: 1,
  cheese: 1
};

/* –ü—Ä–∏–º–µ—Ä –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è */
const mango = new Cashier("Mango", products);

// –ü—Ä–æ–≤–µ—Ä—è–µ–º –∏—Å—Ö–æ–¥–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –ø–æ–ª–µ–π
